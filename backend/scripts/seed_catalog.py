def single(stem: str, options: list[tuple[str, str]], answer: str, explanation: str, difficulty: str, tags: list[str]):
    return {
        "question_type": "single_choice",
        "stem": stem,
        "options": [{"key": key, "text": text} for key, text in options],
        "answer": [answer],
        "explanation": explanation,
        "difficulty": difficulty,
        "tags": tags,
    }


def multiple(stem: str, options: list[tuple[str, str]], answer: list[str], explanation: str, difficulty: str, tags: list[str]):
    return {
        "question_type": "multiple_choice",
        "stem": stem,
        "options": [{"key": key, "text": text} for key, text in options],
        "answer": answer,
        "explanation": explanation,
        "difficulty": difficulty,
        "tags": tags,
    }


def judge(stem: str, answer_true: bool, explanation: str, difficulty: str, tags: list[str]):
    return {
        "question_type": "true_false",
        "stem": stem,
        "options": [{"key": "T", "text": "正确"}, {"key": "F", "text": "错误"}],
        "answer": ["T" if answer_true else "F"],
        "explanation": explanation,
        "difficulty": difficulty,
        "tags": tags,
    }


def short(stem: str, reference: str, explanation: str, difficulty: str, tags: list[str]):
    return {
        "question_type": "short_answer",
        "stem": stem,
        "options": None,
        "answer": [reference],
        "explanation": explanation,
        "difficulty": difficulty,
        "tags": tags,
    }


